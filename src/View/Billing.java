    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // Get data from text fields
    String registrationID = registationNo.getText().trim();
    String carIDText = carID.getText().trim();
    String customerName = cusName.getText().trim();
    String basicPriceText = price.getText().trim();
    String delayFineText = delayFine.getText().trim();
    String finalPriceText = finalPrice.getText().trim();
    
    // Validate data
    if (registrationID.isEmpty() || carIDText.isEmpty() || customerName.isEmpty() || basicPriceText.isEmpty() || delayFineText.isEmpty() || finalPriceText.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please fill in all the fields.");
        return;
    }
    
    try {
        // Parse data
        double basicPrice = Double.parseDouble(basicPriceText);
        double delayFine = Double.parseDouble(delayFineText);
        double finalPrice = Double.parseDouble(finalPriceText);
        
        // Save data to the database
        try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/car_rental", "root", "")) {
            String query = "INSERT INTO returncar (registationID, carID, customerName, basicPrice, delayFine, finalPrice) VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1, registrationID);
            pst.setString(2, carIDText);
            pst.setString(3, customerName);
            pst.setDouble(4, basicPrice);
            pst.setDouble(5, delayFine);
            pst.setDouble(6, finalPrice);
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Data added successfully.");
            
            // Update jTable1 with the new data
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.addRow(new Object[]{registrationID, carIDText, customerName, basicPrice, delayFine, finalPrice});
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error adding data to the database: " + ex.getMessage());
        }
        
        // Clear text fields
        clearFields();
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(null, "Please enter valid numeric values for basic price, delay fine, and final price.");
    }
    }//GEN-LAST:event_addBtnActionPerformed

    private void rentIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rentIDActionPerformed
       String rentIDText = rentID.getText().trim();
    
    if (!rentIDText.isEmpty()) {
        try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/car_rental", "root", "")) {
            String query = "SELECT registationID, carID, customerName, rentDate, dueDate, price FROM rentcar WHERE rentID = ?";
            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1, rentIDText);
            ResultSet rs = pst.executeQuery();
            
            if (rs.next()) {
                // Populate the text fields with the retrieved data
                registationNo.setText(rs.getString("registationID"));
                carID.setText(rs.getString("carID"));
                cusName.setText(rs.getString("customerName"));
                rentDate.setText(rs.getString("rentDate"));
                dueDate.setText(rs.getString("dueDate"));
                price.setText(rs.getString("price"));
            } else {
                // Clear the text fields if no data found for the entered rent ID
                clearFields();
                JOptionPane.showMessageDialog(null, "No data found for the entered rent ID.");
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error retrieving data from the database: " + e.getMessage());
        }
    } else {
        // Clear the text fields if rent ID field is empty
        clearFields();
                                       
    }
    
    }//GEN-LAST:event_rentIDActionPerformed

