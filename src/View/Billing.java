        private void loadDataIntoTable() {
        try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/car_rental", "root", "")) {
            String query = "SELECT * FROM returncar";
            PreparedStatement pst = con.prepareStatement(query);
            ResultSet rs = pst.executeQuery();

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0); // Clear existing data

            while (rs.next()) {
                Object[] row = {
                    rs.getString("registationID"),
                    rs.getString("carID"),
                    rs.getString("customerName"),
                    rs.getDouble("basicPrice"),
                    rs.getDouble("delayFine"),
                    rs.getDouble("finalPrice")
                };
                model.addRow(row);
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error loading data into table: " + e.getMessage());
        }
    
        
    }
    
    private void calculateFinalPrice() {
     try {
        // Parse values from the text fields
        double delayDays = Double.parseDouble(delay.getText().trim());
        double delayFineValue = Double.parseDouble(delayFine.getText().trim());
        double rentDays = Double.parseDouble(rentdays.getText().trim());
        double basicPrice = Double.parseDouble(price.getText().trim());

        // Calculate final price: (basicPrice * rentDays) + (delayDays * delayFineValue)
        double finalPriceValue = (basicPrice * rentDays) + (delayDays * delayFineValue);

        // Display the calculated amount in the finalPrice field
        finalPrice.setText(String.valueOf(finalPriceValue));
    } catch (NumberFormatException e) {
        // Handle invalid input
        JOptionPane.showMessageDialog(null, "Please enter valid numeric values for all fields.");
    }
}


private void saveDataToReturnCarTable() {
    try {
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/car_rental", "root", "");
        String query = "INSERT INTO returncar (registationID, carID, customerName, basicPrice, delayFine, finalPrice) VALUES (?, ?, ?, ?, ?, ?)";
        PreparedStatement pst = con.prepareStatement(query);
        pst.setString(1, registationNo.getText().trim());
        pst.setString(2, carID.getText().trim());
        pst.setString(3, cusName.getText().trim());
        pst.setString(4, price.getText().trim());
        pst.setString(5, delayFine.getText().trim());
        pst.setString(6, finalPrice.getText().trim());
        
        int rowsAffected = pst.executeUpdate();
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(null, "Data saved to returncar table successfully.");
        } else {
            JOptionPane.showMessageDialog(null, "Failed to save data to returncar table.");
        }
        
        con.close();
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error saving data to returncar table: " + e.getMessage());
    }
}

