    public void search() {
    String srch = searchbox.getText();
    
    try {
        String sql = "SELECT * FROM customers WHERE customerName LIKE '%" + srch + "%' OR registrationID LIKE '%" + srch + "%'";
        Connection conn = customerController.getConnections();
        pst = conn.prepareStatement(sql);
        rs = pst.executeQuery();
        customertable.setModel(DbUtils.resultSetToTableModel(rs));
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
}
