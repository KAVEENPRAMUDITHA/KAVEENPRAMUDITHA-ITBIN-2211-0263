    private void customertableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_customertableMouseClicked
        int selectedRow = customertable.getSelectedRow();
    DefaultTableModel model = (DefaultTableModel) customertable.getModel();
    int columnCount = model.getColumnCount();
    
    if (selectedRow != -1) {
        if (columnCount > 0) regiID.setText(model.getValueAt(selectedRow, 0).toString());
        if (columnCount > 1) cusName.setText(model.getValueAt(selectedRow, 1).toString());
        if (columnCount > 2) cusID.setText(model.getValueAt(selectedRow, 2).toString());
        if (columnCount > 3) licence.setText(model.getValueAt(selectedRow, 3).toString());
        if (columnCount > 4) tel.setText(model.getValueAt(selectedRow, 4).toString());
        if (columnCount > 5) address.setText(model.getValueAt(selectedRow, 5).toString());
    }
    }//GEN-LAST:event_customertableMouseClicked

